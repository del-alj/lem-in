#include "ft_lem_in.h"

int     ft_prime(int nb)
{
    int cnt;
    int i;
    int n;

    n = nb;
    i = 2;
    cnt = 0;
    if (n <= 2)
        return (n);
    while (i <= n/2)
    {
        if (n % i != 0)
            i++;
        else
        {
            i = 2;
            while (n % 2 == 0)
                n++;
            cnt++;
            if (cnt == 10)
            return (nb);
        }
    }
    return (n);
}
