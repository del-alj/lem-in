void	ft_if_start_end(t_avl *node, t_data data, t_box *box)
{
	if (data.var == 'S')
	{
		box->start = (t_avl*)malloc(sizeof(t_avl));
		box->start = node;
	}
	else if (data.var == 'E')
	{
		box->end = (t_avl*)malloc(sizeof(t_avl));
		box->end = node;
	}
}

