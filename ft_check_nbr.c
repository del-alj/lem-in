#include "ft_lem_in.h"

int	ft_check_nbr(char *line, int i)
{
	while (line[i] != '\0' && line[i] != ' ')
	{
		if (line[i] < '0' || line[i] > '9')
			return (0);
		i++;
	}
	return (1);
}
