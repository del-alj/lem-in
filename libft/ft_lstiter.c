#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *lxt;

	lxt = lst;
	if (lst)
		while (lxt)
		{
			f(lxt);
			lxt = lxt->next;
		}
}
